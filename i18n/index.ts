export function t(key: string, lang: Lang = 'en'): string {
  const keys = key.split('.');
  let value: any = dict[lang];
  
  for (const k of keys) {
    if (value && typeof value === 'object' && k in value) {
      value = value[k];
    } else {
      return key;
    }
  }
  
  return typeof value === 'string' ? value : key;
}


